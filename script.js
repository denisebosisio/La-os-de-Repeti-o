console.log("Contando de 1 até 10")
for(let i=1; i<=10; i++){
    console.log(i);
}

console.log("Contando de 10 até 1")
let num = 10;
while(num >= 1){
    console.log(num);
    num--;
}

console.log("Contando de 1 até 100")
let contImpar = 1
while(contImpar <= 100){
    console.log(contImpar);
    contImpar++;
}

console.log("Contando de 0 até 100")
for(let i=0; i<=100; i++){
    console.log(i);
}